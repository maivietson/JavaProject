    private void jSPSoChuongStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSPSoChuongStateChanged
        // TODO add your handling code here:
        String itemSelected = String.valueOf(jLMonHoc.getSelectedValue());
        for(Object obj : alMonHoc) {
            if(((MonHoc)obj).getTenMonHoc().equals(itemSelected)) {
                if(((int)jSPSoChuong.getValue()) != ((MonHoc)obj).getSoChuong()) {
                    btnLuu.setEnabled(true);
                    btnHuy.setEnabled(true);
                }
                break;
            }
        }
    }//GEN-LAST:event_jSPSoChuongStateChanged

