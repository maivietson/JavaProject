    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        //Lay thong tin mon hoc dang duoc chon trong danh sach
        MonHoc tmpMonHoc;
        //ArrayList<Object> alMonHoc = new ArrayList<>();
        //FileObject fileObject = new FileObject();
        //alMonHoc = fileObject.readObjectFrFile("MonHoc.txt");
        String itemSelected = String.valueOf(jLMonHoc.getSelectedValue());

        if(!(itemSelected.equals("null")) || !(alMonHoc.isEmpty())) {
            for(Object monHoc : alMonHoc) {
                if(((MonHoc)monHoc).getTenMonHoc().equals(itemSelected)) {
                    tmpMonHoc = (MonHoc)monHoc;
                    tmpMonHoc.setTenMonHoc(jTFTenMonHoc.getText());
                    tmpMonHoc.setMaMonHoc(jTFMaHocPhan.getText());
                    tmpMonHoc.setSoChuong((int)jSPSoChuong.getValue());
                    tmpMonHoc.setGioiThieuMonHoc(jTAGioiThieu.getText());
                    ((DefaultListModel)jLMonHoc.getModel()).setElementAt(jTFTenMonHoc.getText(), jLMonHoc.getSelectedIndex());
                    break;
                }
            }
        }
        if(itemSelected.equals("null"))
        {
            MonHoc newMonHoc = new MonHoc(jTFTenMonHoc.getText(), jTFMaHocPhan.getText(), (int)jSPSoChuong.getValue(), jTAGioiThieu.getText());
            alMonHoc.add(newMonHoc);
            ((DefaultListModel)jLMonHoc.getModel()).addElement(jTFTenMonHoc.getText());
        }
        //Ghi thong tin ra file
        fileObject.writeObjectToFile("MonHoc.txt", alMonHoc);
        btnLuu.setEnabled(false);
        btnHuy.setEnabled(false);
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnXoaMonHocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaMonHocActionPerformed
        // TODO add your handling code here:
        //ArrayList<Object> alMonHoc = new ArrayList<>();
        //FileObject fileObject = new FileObject();
        //alMonHoc = fileObject.readObjectFrFile("MonHoc.txt");
        String itemSelected = String.valueOf(jLMonHoc.getSelectedValue());

        if(!(itemSelected.equals("null")) || !(alMonHoc.isEmpty())) {
            for(Object monHoc : alMonHoc) {
                if(((MonHoc)monHoc).getTenMonHoc().equals(itemSelected)) {
                    ((DefaultListModel)jLMonHoc.getModel()).removeElement(((MonHoc)monHoc).getTenMonHoc());
                    clearAll();
                    alMonHoc.remove(monHoc);
                    fileObject.writeObjectToFile("MonHoc.txt", alMonHoc);
                    break;
                }
            }
        }
        btnXoaMonHoc.setEnabled(false);
    }//GEN-LAST:event_btnXoaMonHocActionPerformed

