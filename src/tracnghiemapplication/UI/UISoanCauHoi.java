    private void jLBChinhSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLBChinhSuaMouseClicked
        // TODO add your handling code here:
        boolean isTrue = false;
        if(!(jLDapAn.getSelectedValue().isEmpty())) {
            btnLuu.setEnabled(true);
            btnHuy.setEnabled(true);
            String itemSelected = jLDapAn.getSelectedValue();
            for(String dad : dapAnDung) {
                if(dad.equals(itemSelected)) {
                    isTrue = true;
                    dapAnDung.remove(dad);
                    break;
                }
            }
            UINhapDanAn nhapDapAn = new UINhapDanAn();
            nhapDapAn.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            //nhapDapAn.suaGiaTri(itemSelected);
            nhapDapAn.getJTextField().setText(itemSelected);
            nhapDapAn.getJCheckBox().setSelected(isTrue);
            nhapDapAn.setVisible(true);
            nhapDapAn.getJButton().addActionListener((ActionEvent e) -> {
                ((DefaultListModel)this.getJList().getModel()).setElementAt(nhapDapAn.getJTextField().getText(), this.getJList().getSelectedIndex());
                if(nhapDapAn.getJCheckBox().isSelected()) {
                    dapAnDung.add(nhapDapAn.getJTextField().getText());
                }
                nhapDapAn.dispose();
            });
        }
    }//GEN-LAST:event_jLBChinhSuaMouseClicked

