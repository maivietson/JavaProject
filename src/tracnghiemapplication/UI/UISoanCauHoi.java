    private void jLCauHoiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLCauHoiMouseClicked
        // TODO add your handling code here:
        cleanAll();
        btnXoaCauHoi.setEnabled(true);
        String itemSelected = String.valueOf(jLCauHoi.getSelectedValue());
        
        for(CauHoi cauHoi : monHoc.getDSCauHoi()) {
            if(cauHoi.getCauHoi().equals(itemSelected)) {

                dapAnDung.clear();
                dapAnDung.addAll(cauHoi.getDapAnDung());

                if(cauHoi instanceof CauHoiTracNghiem) {
                    jRBTracNghiem.setSelected(true);
                    jRBTuLuan.setSelected(false);
                }
                if(cauHoi instanceof CauHoiTuLuan) {
                    jRBTuLuan.setSelected(true);
                    jRBTracNghiem.setSelected(false);
                }
                jTADeBai.setText(cauHoi.getCauHoi());
                jTFDoKho.setText(String.valueOf(cauHoi.getDoKho()));
                jCBChuong.setSelectedIndex(cauHoi.getSoChuong() - 1);

                for(String da : cauHoi.getDapAn()) {
                    ((DefaultListModel)jLDapAn.getModel()).addElement(da);
                }
                break;
            }
        }
    }//GEN-LAST:event_jLCauHoiMouseClicked

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        String itemSelected = String.valueOf(jLCauHoi.getSelectedValue());
        FileObject fileObject = new FileObject();
        ArrayList<String> tmpDapAn = new ArrayList<>();
        if(!(itemSelected.equals("null"))) {
            if(jRBTracNghiem.isSelected() || jRBTuLuan.isSelected()) {
                for(CauHoi cauHoi : monHoc.getDSCauHoi()) {
                    if(cauHoi.getCauHoi().equals(itemSelected)) {
                        String tmpCauHoi = jTADeBai.getText().replaceAll("\n", " ");
                        cauHoi.setCauHoi(tmpCauHoi);
                        cauHoi.setDoKho(Integer.parseInt(jTFDoKho.getText()));
                        cauHoi.setSoChuong(jCBChuong.getSelectedIndex() + 1);
                        for(int i = 0; i < ((DefaultListModel)jLDapAn.getModel()).getSize(); i++) {
                            tmpDapAn.add((String) ((DefaultListModel)jLDapAn.getModel()).getElementAt(i));
                        }
                        cauHoi.setDapAn(tmpDapAn);
                        cauHoi.setDapAnDung(dapAnDung);
                        break;
                    }
                }
            }
        }
        if(itemSelected.equals("null")) {
            if(jRBTracNghiem.isSelected() && !(jTADeBai.getText().isEmpty()) && !(jTFDoKho.getText().isEmpty())) {
                CauHoiTracNghiem tmpTracNghiem = new CauHoiTracNghiem(jTADeBai.getText(), jCBChuong.getSelectedIndex() + 1, Integer.parseInt(jTFDoKho.getText()));
                for(int i = 0; i < ((DefaultListModel)jLDapAn.getModel()).getSize(); i++) {
                    tmpDapAn.add((String) ((DefaultListModel)jLDapAn.getModel()).getElementAt(i));
                }
                tmpTracNghiem.setDapAn(tmpDapAn);
                tmpTracNghiem.setDapAnDung(dapAnDung);
                monHoc.themCauHoi(tmpTracNghiem);
                ((DefaultListModel)jLCauHoi.getModel()).addElement(jTADeBai.getText()); 
            }
            else if(jRBTuLuan.isSelected() && !(jTADeBai.getText().isEmpty()) && !(jTFDoKho.getText().isEmpty())) {
                CauHoiTuLuan tmpTuLuan = new CauHoiTuLuan(jTADeBai.getText(), jCBChuong.getSelectedIndex() + 1, Integer.parseInt(jTFDoKho.getText()));
                for(int i = 0; i < ((DefaultListModel)jLDapAn.getModel()).getSize(); i++) {
                    tmpDapAn.add((String) ((DefaultListModel)jLDapAn.getModel()).getElementAt(i));
                }
                tmpTuLuan.setDapAn(tmpDapAn);
                tmpTuLuan.setDapAnDung(dapAnDung);
                monHoc.themCauHoi(tmpTuLuan);
                ((DefaultListModel)jLCauHoi.getModel()).addElement(jTADeBai.getText()); 
            }
            else {
                JOptionPane.showMessageDialog(null, "Bạn điền chưa đủ thông tin! Vui lòng kiểm tra lại", "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
        fileObject.updateObjectInFile("MonHoc.txt", monHoc);
        btnLuu.setEnabled(false);
        btnHuy.setEnabled(false);
    }//GEN-LAST:event_btnLuuActionPerformed

