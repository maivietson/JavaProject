    private void jLabel11MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel11MouseClicked
        // TODO add your handling code here:
        String cauHoi = jTADeBai.getText();
        ArrayList<CauHoi> cauHoiTuongDuong = new ArrayList<>();
        if(!(cauHoi.isEmpty())) {
            ArrayList<String> wordSource = new ArrayList<>(Arrays.asList(cauHoi.split(" ")));
            wordSource.replaceAll((String word) -> word.toUpperCase());
            ArrayList<String> wordDestination;
            for(CauHoi ch : monHoc.getDSCauHoi()) {
                int count = 0;
                wordDestination = new ArrayList<>(Arrays.asList(ch.getCauHoi().split(" ")));
                wordDestination.replaceAll((String word) -> word.toUpperCase());
                for(String word : wordSource) {
                    if(wordDestination.contains(word)) {
                        count++;
                    }
                }
                if(((double)count/wordDestination.size()) > 0.7) {
                    cauHoiTuongDuong.add(ch);
                }
            }
            UICauHoiTuongDuong cauHoiTD = new UICauHoiTuongDuong();
            cauHoiTD.setCauHoiTuongDuong(cauHoiTuongDuong);
            cauHoiTD.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            cauHoiTD.setVisible(true);
        }
    }//GEN-LAST:event_jLabel11MouseClicked

